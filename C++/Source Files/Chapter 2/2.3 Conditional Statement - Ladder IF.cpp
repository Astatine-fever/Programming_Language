// Ladder If using C++
#include<iostream>
using namespace std;
int main()
{
	int f;
	cout<<"enter a f value\n";
	cin>>f;
if(f>=0 && f<20)
	{
	 cout<<"f value is greater then 0";
	}
else if(f>=20 && f<30)
	{
	 cout<<"f value is greater than 20";
	}
else if(f>=30)
	{
	 cout<<"f value is greater than 30";
	}

else
	{
	 cout<<"f value is less then 0";
	}
}

